interface AccordionProps {
  title: string;
  children: React.ReactNode;
  defaultOpen?: boolean;
}

function Accordion({ title, children, defaultOpen = false }: AccordionProps) {
  const [open, setOpen] = useState(defaultOpen);
  return (
    <View style={styles.accordionContainer}>
      <TouchableOpacity onPress={() => setOpen((v) => !v)} style={styles.accordionHeader}>
        <Text style={styles.accordionTitle}>{title}</Text>
        <FontAwesome5 name={open ? 'chevron-up' : 'chevron-down'} size={16} color={colors.accent} />
      </TouchableOpacity>
      {open && <View style={styles.accordionContent}>{children}</View>}
    </View>
  );
}

type KnowledgeItem = string | { title: string; items?: KnowledgeItem[]; children?: KnowledgeItem[] };

function renderItems(items: KnowledgeItem[], level = 0): React.ReactNode {
  return items.map((item, idx) => {
    if (typeof item === 'string') {
      return (
        <View key={item + idx} style={{ marginLeft: level * 12, marginBottom: 6 }}>
          <Text style={styles.itemText}>‚Ä¢ {item}</Text>
        </View>
      );
    }
    if (item.items) {
      return (
        <View key={item.title + idx} style={{ marginBottom: 12 }}>
          <Text style={[styles.itemTitle, { marginLeft: level * 8 }]}>{item.title}</Text>
          {renderItems(item.items, level + 1)}
        </View>
      );
    }
    if (item.children) {
      return (
        <View key={item.title + idx} style={{ marginBottom: 12 }}>
          <Text style={[styles.itemTitle, { marginLeft: level * 8 }]}>{item.title}</Text>
          {item.children.map((child, cidx) => renderItems([child], level + 1))}
        </View>
      );
    }
    return null;
  });
}

